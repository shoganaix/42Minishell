#include "utils.h"

/**
 * is <, > or |
 */
int	isreserved(char c)
{
	return (c == '<' || c == '>' || c == '|');
}

/**
 * is <, >, |, " or '
 */
int	isreservedall(char c)
{
	return (c == '<' || c == '>' || c == '|'
		|| c == '"' || c == '\'');
}

int	isquote(char c)
{
	return (c == '"' || c == '\'');
}
